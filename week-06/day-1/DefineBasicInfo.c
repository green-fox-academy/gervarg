#include <stdio.h>
#include <stdlib.h>

int main()
{
    // Define several things as a variable then print their values
    // First letter of your name as a char
    // Your age as an integer
    // Your height in meters as a double
    // Your favourite positive number as an unsigned int
    char first_letter = 'G';
    int age = 33;
    float height = 1.89;
    unsigned int favorite_number = 24;

    printf("First letter of my name: %c, my age: %d, my height %f, my favorite number: %d", first_letter, age, height, favorite_number);

    return 0;
}
